// A class to store information related to a Team

class Team {
    /**
     * Constructor
     * @param {Number} teamNumber The team number
     * @param {String} teamName The name of the team
     * @param {String} teamColor The color of the team
     * @param {String} teamLogo The logo of the team
     */
    constructor(teamNumber, teamName, teamColor, teamLogo) {
      this.teamNumber = teamNumber;
      this.teamName = teamName;
      this.teamColor = teamColor;
      this.teamLogo = teamLogo;

  
    }
  
  }